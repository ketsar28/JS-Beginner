var noAngkot = 1;
// menggunakan salah satu fungsi pengulangan pada js yaitu 'while'

while (noAngkot <= 10) {
  // jika kondisi pada while bernilai true, maka jalankan aksi dibawah ini
  console.log("Angkot No. " + noAngkot + " Beroperasi Dengan Baik");
  noAngkot++;
  // jika tidak terjebak kedalam infinite loop/looping forever, sebaiknya menggunakan yang namanya increment (++) /decrement (--)
}
