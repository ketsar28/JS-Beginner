// Refactoring : sebuah teknik/proses mengubah kode agar menjadi lebih 'baik', tanpa mengubah fungsionalitasnya

/* 
Manfaat Teknik Refactoring :

1. Readability : Membuat code program mudah dibaca /dipahami

2. DRY (Don't Repeat Yourself) : ini terkait dengan duplikasi code program. Misalnya efesiensi penggunaan function, Method, dll.

3. Testability : berkaitan dengan penulisan code, yang mana code yang di susun dengan baik dan rapi agar mudah ketika diuji.

4. Performance : menulis code supaya dapat meningkatkan performency pada programmnya. Misalnya : penggunaan looping dengan baik, memilih tipe data yang tepat, dan pengalokasian memory yang efektif  pada bahasa pemrograman tertentu.

5. Maintainbility : bagaimana nantinya program kita dapat dengan mudah dikelola atau dikembangkan 
*/
